export function getCategoryIcon(label?: string | null, key?: string | null): string {
  const normalizedLabel = (label || '').trim();
  const normalizedKey = (key || '').trim().toLowerCase();

  if (normalizedLabel && EXACT_ICON_BY_LABEL[normalizedLabel]) {
    return EXACT_ICON_BY_LABEL[normalizedLabel];
  }

  for (const [keywords, icon] of KEYWORD_ICON_RULES) {
    if (keywords.some((keyword) => normalizedKey.includes(keyword))) {
      return icon;
    }
  }

  if (/уборк|clean/i.test(normalizedLabel)) return '🧹';
  if (/сантех|plumb/i.test(normalizedLabel)) return '🔧';
  if (/электр|electr/i.test(normalizedLabel)) return '⚡';
  if (/ремонт|repair/i.test(normalizedLabel)) return '🛠️';
  if (/достав|курьер|переезд/i.test(normalizedLabel)) return '📦';
  if (/сад|ландшафт|газон/i.test(normalizedLabel)) return '🌿';
  if (/красот|космет|макияж|маник|парик|массаж/i.test(normalizedLabel)) return '💄';
  if (/видео|фото/i.test(normalizedLabel)) return '📸';
  if (/юрид|нотари/i.test(normalizedLabel)) return '⚖️';

  return '🛠️';
}
